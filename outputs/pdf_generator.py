"""
PDF Brief Generator — Phase 3
===============================
What this file does:
  - Reads the AI-generated brief JSON from Phase 2
  - Generates a clean, professional PDF report
  - Saves it to outputs/pdfs/ folder

Think of this as the "publisher" of the engine.
Phase 1 collected data. Phase 2 analyzed it.
Phase 3 packages it into something you can hand to anyone.

Run it:
  python outputs/pdf_generator.py --ticker TSLA
  python outputs/pdf_generator.py --ticker MSFT
"""

import json
import argparse
from pathlib import Path
from datetime import datetime

from reportlab.lib.pagesizes import letter
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import inch
from reportlab.lib import colors
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer,
    Table, TableStyle, HRFlowable
)
from reportlab.lib.enums import TA_LEFT, TA_CENTER, TA_RIGHT

# ── Configuration ─────────────────────────────────────────────────────────────

INPUT_DIR  = Path("data/briefs")
OUTPUT_DIR = Path("outputs/pdfs")

# Brand colors — professional dark blue palette
COLOR_DARK      = colors.HexColor("#0A1628")   # Near black — headers
COLOR_PRIMARY   = colors.HexColor("#1B4F8A")   # Deep blue — section titles
COLOR_ACCENT    = colors.HexColor("#2E86C1")   # Medium blue — highlights
COLOR_LIGHT     = colors.HexColor("#EAF2FB")   # Light blue — backgrounds
COLOR_SUCCESS   = colors.HexColor("#1E8449")   # Green — positive signals
COLOR_WARNING   = colors.HexColor("#B7950B")   # Amber — risk flags
COLOR_BORDER    = colors.HexColor("#BDC3C7")   # Gray — dividers
COLOR_TEXT      = colors.HexColor("#2C3E50")   # Dark gray — body text
COLOR_MUTED     = colors.HexColor("#7F8C8D")   # Light gray — metadata


# ── Step 1: Load the brief JSON from Phase 2 ─────────────────────────────────

def load_brief(ticker):
    """Load the intelligence brief generated by Phase 2."""
    filepath = INPUT_DIR / f"{ticker.upper()}_brief.json"

    if not filepath.exists():
        raise FileNotFoundError(
            f"No brief found for {ticker.upper()}. "
            f"Run Phase 2 first: python engine/synthesizer.py --ticker {ticker}"
        )

    with open(filepath, encoding="utf-8") as f:
        data = json.load(f)

    # Handle both direct brief and wrapped brief
    brief = data.get("brief", data)
    if "company_name" not in brief:
        brief = data

    print(f"  Loaded brief for: {brief.get('company_name', ticker)}")
    return brief, data.get("generated_at", "")


